DROP TABLE IF EXISTS thumbChecked;
CREATE TABLE thumbChecked (
     id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
     userid INTEGER REFERENCES users(id) ON DELETE SET NULL,
     place_id INTEGER REFERENCES place(id) ON DELETE CASCADE NULL,
     review_id INTEGER REFERENCES review(id) ON DELETE CASCADE NULL,
     thumb INTEGER REFERENCES thumbText(id) ON DELETE SET NULL
);

