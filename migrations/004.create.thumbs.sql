DROP TABLE IF EXISTS thumbText;
CREATE TABLE thumbText (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    description TEXT
)


DROP TABLE IF EXISTS thumbChecked;
CREATE TABLE thumbChecked (
     id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
     userId INTEGER REFERENCES users(id) ON DELETE SET NOT NULL,
     placeId INTEGER REFERENCES place(id) ON DELETE CASCADE NULL,
     reviewId INTEGER REFERENCES review(id) ON DELETE CASCADE NULL,
     thumb INTEGER REFERENCES thumbText(description) ON DELETE SET NULL,
)

